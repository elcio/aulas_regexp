from ipv4validator import validate

assert validate('10.0.0.1')
assert validate('255.255.255.255')
assert validate('255.0.0.9')
assert validate('137.43.2.1')
assert validate('1.0.0.0')
assert validate('123.45.219.89')
assert validate('123.45.249.89')
assert validate('123.45.29.249')
assert not validate('256.0.0.1')
assert not validate('0.0.256.1')
assert not validate('1.256.4.3')
assert not validate('1.2.3.256')
assert not validate('1000.2.3.4')
assert not validate('3.4.1')
assert not validate('3.55')
assert not validate('-45.23.5.8')
assert not validate(' 23.12.44.98')
assert not validate('123.45.+67.89')
assert not validate('123.$45.67.89')
assert not validate('123.45.#67.89')
assert not validate('123.45.a67.89')
assert not validate('123.45._67.89')
